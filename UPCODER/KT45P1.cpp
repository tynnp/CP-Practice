#include <bits/stdc++.h>
using namespace std;

int main() {
    int a, b, c;
    cin >> a >> b >> c;

    cout << a + b << endl;
    cout << a - b << endl;
    cout << a * b << endl;
    cout << a / b << endl;
    cout << a % b << endl;
    cout << a * 3.14 << endl;
    cout << 3.14 * a * a / 4.0 << endl;
    cout << (c + b) * 2 << endl;
    cout << c * b << endl;
    
    return 0;
}